package snap.ib.personelLoan.adapter.common.beans;

public class Scheduled_List {

	private String Installment_No;
	private String Principal;
	private String Interest;
	private String Recd_Amount;
	private String EMI_Amount;
	private String EMI_Date;
	private String EMI_Billed;
	

	public String getInstallment_No() {
		return Installment_No;
	}
	public void setInstallment_No(String installment_No) {
		Installment_No = installment_No;
	}
	public String getPrincipal() {
		return Principal;
	}
	public void setPrincipal(String principal) {
		Principal = principal;
	}
	public String getInterest() {
		return Interest;
	}
	public void setInterest(String interest) {
		Interest = interest;
	}
	public String getRecd_Amount() {
		return Recd_Amount;
	}
	public void setRecd_Amount(String recd_Amount) {
		Recd_Amount = recd_Amount;
	}
	public String getEMI_Amount() {
		return EMI_Amount;
	}
	public void setEMI_Amount(String eMI_Amount) {
		EMI_Amount = eMI_Amount;
	}
	public String getEMI_Date() {
		return EMI_Date;
	}
	public void setEMI_Date(String eMI_Date) {
		EMI_Date = eMI_Date;
	}
	public String getEMI_Billed() {
		return EMI_Billed;
	}
	public void setEMI_Billed(String eMI_Billed) {
		EMI_Billed = eMI_Billed;
	}

}
