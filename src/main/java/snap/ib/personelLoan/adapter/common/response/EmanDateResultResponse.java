package snap.ib.personelLoan.adapter.common.response;

public class EmanDateResultResponse {
	
	private String success;
	private String last_4_digit_of_account;
	private String ifsc_code;
	private String frequency;
	private String error_message;
	private String emi_amount;
	private String bank_name;
	private String customer_name;
	
	
	
	public String getSuccess() {
		return success;
	}
	public void setSuccess(String success) {
		this.success = success;
	}
	public String getLast_4_digit_of_account() {
		return last_4_digit_of_account;
	}
	public void setLast_4_digit_of_account(String last_4_digit_of_account) {
		this.last_4_digit_of_account = last_4_digit_of_account;
	}
	public String getIfsc_code() {
		return ifsc_code;
	}
	public void setIfsc_code(String ifsc_code) {
		this.ifsc_code = ifsc_code;
	}
	public String getFrequency() {
		return frequency;
	}
	public void setFrequency(String frequency) {
		this.frequency = frequency;
	}
	public String getError_message() {
		return error_message;
	}
	public void setError_message(String error_message) {
		this.error_message = error_message;
	}
	public String getEmi_amount() {
		return emi_amount;
	}
	public void setEmi_amount(String emi_amount) {
		this.emi_amount = emi_amount;
	}
	public String getBank_name() {
		return bank_name;
	}
	public void setBank_name(String bank_name) {
		this.bank_name = bank_name;
	}
	public String getCustomer_name() {
		return customer_name;
	}
	public void setCustomer_name(String customer_name) {
		this.customer_name = customer_name;
	}
	
	
	
	
}
