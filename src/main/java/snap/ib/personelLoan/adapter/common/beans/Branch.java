package snap.ib.personelLoan.adapter.common.beans;

public class Branch {

	private String bank_name;
	private String bank_state;
	private String bank_city;
	private String branch_name;
	private String branch_address;
	private String branch_ifsc_code;

	public String getBank_name() {
		return bank_name;
	}

	public void setBank_name(String bank_name) {
		this.bank_name = bank_name;
	}

	public String getBank_state() {
		return bank_state;
	}

	public void setBank_state(String bank_state) {
		this.bank_state = bank_state;
	}

	public String getBank_city() {
		return bank_city;
	}

	public void setBank_city(String bank_city) {
		this.bank_city = bank_city;
	}

	public String getBranch_name() {
		return branch_name;
	}

	public void setBranch_name(String branch_name) {
		this.branch_name = branch_name;
	}

	public String getBranch_address() {
		return branch_address;
	}

	public void setBranch_address(String branch_address) {
		this.branch_address = branch_address;
	}

	public String getBranch_ifsc_code() {
		return branch_ifsc_code;
	}

	public void setBranch_ifsc_code(String branch_ifsc_code) {
		this.branch_ifsc_code = branch_ifsc_code;
	}
}
