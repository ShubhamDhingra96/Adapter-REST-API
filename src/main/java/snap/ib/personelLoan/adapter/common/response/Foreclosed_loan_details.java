public class Foreclosed_loan_details {

	
	private String foreclosed_loan_lead_id;
	private String foreclosed_loan_cas_id;
	private boolean foreclosed_loan_has_top_up;
	
	
	public String getForeclosed_loan_lead_id() {
		return foreclosed_loan_lead_id;
	}
	public void setForeclosed_loan_lead_id(String foreclosed_loan_lead_id) {
		this.foreclosed_loan_lead_id = foreclosed_loan_lead_id;
	}
	public String getForeclosed_loan_cas_id() {
		return foreclosed_loan_cas_id;
	}
	public void setForeclosed_loan_cas_id(String foreclosed_loan_cas_id) {
		this.foreclosed_loan_cas_id = foreclosed_loan_cas_id;
	}
	public boolean isForeclosed_loan_has_top_up() {
		return foreclosed_loan_has_top_up;
	}
	public void setForeclosed_loan_has_top_up(boolean foreclosed_loan_has_top_up) {
		this.foreclosed_loan_has_top_up = foreclosed_loan_has_top_up;
	}
	
	
	
}
