package snap.ib.personelLoan.adapter.common.beans;

public class UserData {
	
	private String MessageDateSent;
	private String DeviceSmsId;
	private String Message;
	private String MessageSource;
	private String MessageDate;

	public String getMessageDateSent() {
		return MessageDateSent;
	}

	public void setMessageDateSent(String messageDateSent) {
		MessageDateSent = messageDateSent;
	}

	public String getDeviceSmsId() {
		return DeviceSmsId;
	}

	public void setDeviceSmsId(String deviceSmsId) {
		DeviceSmsId = deviceSmsId;
	}

	public String getMessage() {
		return Message;
	}

	public void setMessage(String message) {
		Message = message;
	}

	public String getMessageSource() {
		return MessageSource;
	}

	public void setMessageSource(String messageSource) {
		MessageSource = messageSource;
	}

	public String getMessageDate() {
		return MessageDate;
	}

	public void setMessageDate(String messageDate) {
		MessageDate = messageDate;
	}
}
